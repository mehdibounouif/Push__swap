#include "../push_swap.h"

void	move_a_b(t_stack **a, t_stack **b)
{
	int	mid;

	mid = ft_list_size(*a) / 3;
	while ((*a)->rank > mid)
		rotate(a, 1);
	push(b, a, 0);
}
